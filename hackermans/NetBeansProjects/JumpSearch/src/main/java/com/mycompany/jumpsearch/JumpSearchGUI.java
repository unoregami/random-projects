    private void tfNumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfNumberActionPerformed
        i++;
        array = java.util.Arrays.copyOf(array, i);
        array[i-1] = Integer.parseInt(tfNumber.getText());
        if (i == 1) {
            tfArray.setText(tfArray.getText() + tfNumber.getText());
        } else {
            tfArray.setText(tfArray.getText() + ", " + tfNumber.getText());
        }
        tfNumber.setText(null);
    }//GEN-LAST:event_tfNumberActionPerformed
    
    private void btnKeyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKeyActionPerformed
        try {
            int x = Integer.parseInt(tfKey.getText());
            tfKey.setText(null);
            key = isKeyInArray(x);
        
            if (key == -1) {
                tfResult.setText(x + " is not in the array");
            } else {
                int ret = jumpSearch();
                if (ret == -1){
                    tfResult.setText("Array is not sorted!");
                } else {
                    tfResult.setText(String.valueOf("Key " + key + " is at position " + ret));
                }
            }
        } catch (Exception e) {
            tfResult.setText("Error");
        }
    }//GEN-LAST:event_btnKeyActionPerformed

