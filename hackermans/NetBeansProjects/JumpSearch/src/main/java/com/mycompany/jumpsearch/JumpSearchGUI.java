    public int jumpSearch() {
        int n = array.length;
        int step = (int)Math.sqrt(array.length);    //step is the jump per index
        int prev = 0;   //stores the previous jump index
        
        for (int j = step; array[j] < key;) {
            if (array[j] == key) {
                return j;
            }
            prev = (j + 1); //inc by one since it is confirmed that i is not the position of x
            j+=step;        //inc is moved here since if array is out of bounds we can break it without triggering the ArrayOutOfBounds Exception in line 16
            if (j >= n) {
                break;
            }
        }
        
        while (array[prev] < key) {
            prev++;
        }
        
        if (array[prev] == key) {
                return prev;
            }
        return - 1;
    }
    public  int isKeyInArray(int x) {
        for (int j = 0; j < array.length; j++) {
            if (array[j] != x) {
                if (j == (array.length - 1)) {
                    return -1;
                }
                continue;
            }
            break;
        }
        return x;
    }
    public void reset() {
        array = new int[0];
        key = 0;
        i = 0;
        
        tfArray.setText("Array: ");
        tfNumber.setText(null);
        tfKey.setText(null);
        tfResult.setText(null);
    }
    
